public class ifelse0407 {
    public static void main(String[] args){
        int x = 2;
        switch (x){
            case 1:System.out.println(x+":Grade:D");
                   break; 
            case 2:System.out.println(x+":Grade:C");
                   break; 
            case 3:System.out.println(x+":Grade:B");
                   break; 
            case 4:System.out.println(x+":Grade:A");
                   break; 
            default:System.out.println(x+":Grade:E");
                   break; 
        }
    }
}
